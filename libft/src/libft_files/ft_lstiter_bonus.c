#include "../../inc/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	temp = lst;
	if (lst != NULL || f != NULL)
	{
		while (temp)
		{
			f(temp->content);
			temp = temp->next;
		}
	}
}
